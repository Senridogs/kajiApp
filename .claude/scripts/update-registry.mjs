#!/usr/bin/env node
/**
 * update-registry.mjs
 *
 * .claude/REGISTRY.md を自動生成するスクリプト。
 * 情報ソース:
 *   - PJスキル: .claude/skills/*.md (frontmatter description)
 *   - グローバルスキル: 組み込み定数
 *   - ルール: .claude/rules/*.md (先頭の # タイトル行)
 *   - Hooks: .claude/settings.local.json の hooks セクション
 *
 * 外部依存なし（Node.js 標準APIのみ）。
 */

import { readdir, readFile, writeFile } from 'node:fs/promises'
import { join, basename, resolve } from 'node:path'

const ROOT = resolve(import.meta.dirname, '..', '..')
const CLAUDE_DIR = join(ROOT, '.claude')
const SKILLS_DIR = join(CLAUDE_DIR, 'skills')
const RULES_DIR = join(CLAUDE_DIR, 'rules')
const SETTINGS_PATH = join(CLAUDE_DIR, 'settings.local.json')
const OUTPUT_PATH = join(CLAUDE_DIR, 'REGISTRY.md')

// --- 引数チェック: 対象外なら即終了 ---

const toolInput = process.argv[2] || ''
if (toolInput) {
  const normalized = toolInput.replace(/\\/g, '/')
  const targets = ['.claude/skills/', '.claude/rules/', 'settings.local.json', '.claude\\skills\\', '.claude\\rules\\']
  const isTarget = targets.some((t) => normalized.includes(t))
  if (!isTarget) {
    process.exit(0)
  }
}

// --- グローバルスキル定数 ---

const GLOBAL_SKILLS = [
  { command: '/plan', description: '要件整理・実装計画' },
  { command: '/tdd', description: 'TDD開発ワークフロー' },
  { command: '/code-review', description: 'コードレビュー' },
  { command: '/build-fix', description: 'ビルドエラー修正' },
  { command: '/e2e', description: 'E2Eテスト生成・実行' },
  { command: '/test-coverage', description: 'テストカバレッジ確認' },
  { command: '/refactor-clean', description: 'デッドコード除去' },
  { command: '/update-docs', description: 'ドキュメント更新' },
  { command: '/update-codemaps', description: 'コードマップ更新' },
  { command: '/learn', description: 'パターン抽出・学習' },
]

// --- ユーティリティ ---

function extractFrontmatter(content) {
  const match = content.match(/^---\s*\n([\s\S]*?)\n---/)
  if (!match) return {}
  const fm = {}
  for (const line of match[1].split('\n')) {
    const sep = line.indexOf(':')
    if (sep === -1) continue
    const key = line.slice(0, sep).trim()
    const val = line.slice(sep + 1).trim()
    fm[key] = val
  }
  return fm
}

function extractFirstHeading(content) {
  const match = content.match(/^#\s+(.+)$/m)
  return match ? match[1].trim() : ''
}

function inferArgs(content) {
  if (!content.includes('$ARGUMENTS')) return 'なし'
  const backtickArg = content.match(/`<([^>]+)>`/)
  if (backtickArg) return `\`<${backtickArg[1]}>\``
  const argLine = content.match(/`?\$ARGUMENTS`?\s*=\s*(.+)/m)
  if (argLine) {
    const desc = argLine[1].replace(/[（(].+$/, '').replace(/`/g, '').trim()
    return `\`${desc}\``
  }
  return 'あり'
}

// --- スキャナー ---

async function scanProjectSkills() {
  const skills = []
  let files
  try {
    files = await readdir(SKILLS_DIR)
  } catch {
    return skills
  }
  for (const file of files.sort()) {
    if (!file.endsWith('.md')) continue
    const name = basename(file, '.md')
    const content = await readFile(join(SKILLS_DIR, file), 'utf-8')
    const fm = extractFrontmatter(content)
    const description = fm.description || extractFirstHeading(content) || name
    const args = inferArgs(content)
    skills.push({ command: `/${name}`, description, args })
  }
  return skills
}

async function scanRules() {
  const rules = []
  let files
  try {
    files = await readdir(RULES_DIR)
  } catch {
    return rules
  }
  for (const file of files.sort()) {
    if (!file.endsWith('.md')) continue
    const content = await readFile(join(RULES_DIR, file), 'utf-8')
    const heading = extractFirstHeading(content)
    rules.push({ file, description: heading || file })
  }
  return rules
}

async function scanHooks() {
  const hooks = []
  let settings
  try {
    const raw = await readFile(SETTINGS_PATH, 'utf-8')
    settings = JSON.parse(raw)
  } catch {
    return hooks
  }
  const hooksConfig = settings.hooks
  if (!hooksConfig || typeof hooksConfig !== 'object') return hooks

  for (const [event, entries] of Object.entries(hooksConfig)) {
    if (!Array.isArray(entries)) continue
    for (const entry of entries) {
      const matcher = (entry.matcher || '*').replace(/\|/g, '\\|')
      const hookList = entry.hooks
      if (!Array.isArray(hookList)) continue
      for (const hook of hookList) {
        const cmd = hook.command || hook.type || '(不明)'
        const shortCmd = cmd.length > 80 ? cmd.slice(0, 77) + '...' : cmd
        hooks.push({ event, matcher, action: shortCmd })
      }
    }
  }
  return hooks
}

// --- テンプレート生成 ---

function buildMarkdown(projectSkills, globalSkills, rules, hooks) {
  const now = new Date().toISOString().slice(0, 10)
  const lines = []

  lines.push('# Claude Code Registry')
  lines.push(`<!-- AUTO-GENERATED by update-registry.mjs — 手動編集禁止 -->`)
  lines.push(`<!-- 最終更新: ${now} -->`)
  lines.push('')

  // PJスキル
  lines.push('## スキル（プロジェクト）')
  lines.push('')
  if (projectSkills.length === 0) {
    lines.push('（なし）')
  } else {
    lines.push('| コマンド | 用途 | 引数 |')
    lines.push('|---------|------|------|')
    for (const s of projectSkills) {
      lines.push(`| ${s.command} | ${s.description} | ${s.args} |`)
    }
  }
  lines.push('')

  // グローバルスキル
  lines.push('## スキル（グローバル）')
  lines.push('')
  lines.push('| コマンド | 用途 |')
  lines.push('|---------|------|')
  for (const s of globalSkills) {
    lines.push(`| ${s.command} | ${s.description} |`)
  }
  lines.push('')

  // ルール
  lines.push('## ルール')
  lines.push('')
  if (rules.length === 0) {
    lines.push('（なし）')
  } else {
    lines.push('| ファイル | 用途 |')
    lines.push('|---------|------|')
    for (const r of rules) {
      lines.push(`| ${r.file} | ${r.description} |`)
    }
  }
  lines.push('')

  // Hooks
  lines.push('## Hooks')
  lines.push('')
  if (hooks.length === 0) {
    lines.push('（なし）')
  } else {
    lines.push('| イベント | トリガー | 動作 |')
    lines.push('|---------|---------|------|')
    for (const h of hooks) {
      lines.push(`| ${h.event} | ${h.matcher} | ${h.action} |`)
    }
  }
  lines.push('')

  return lines.join('\n')
}

// --- メイン ---

async function main() {
  const [projectSkills, rules, hooks] = await Promise.all([
    scanProjectSkills(),
    scanRules(),
    scanHooks(),
  ])

  const md = buildMarkdown(projectSkills, GLOBAL_SKILLS, rules, hooks)
  await writeFile(OUTPUT_PATH, md, 'utf-8')

  const total = projectSkills.length + GLOBAL_SKILLS.length + rules.length + hooks.length
  console.log(`REGISTRY.md updated: ${projectSkills.length} PJ skills, ${GLOBAL_SKILLS.length} global skills, ${rules.length} rules, ${hooks.length} hooks (total: ${total})`)
}

main().catch((err) => {
  console.error('update-registry.mjs failed:', err.message)
  process.exit(1)
})
